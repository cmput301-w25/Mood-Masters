package models;

public class Square extends Shape {
    String color = "blue";
    private double side;

    public Square(double side) {
        this.side = side;
    }

    public double getSide() {
        return side;
    }

    public void setSide(double side) {
        this.side = side;
    }

    @Override
    public String toString() {
        return "Square with side length " + side;
    }
}
